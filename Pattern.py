class Pattern:
    def __init__(self):
        self.code = None
        self.pattern = None
    
    # METODOS GET
    def getCode(self):
        return self.code

    def getPattern(self):
        return self.pattern
    
    # METODOS SET
    def setCode(self, code):
        self.code = code
    
    def setPattern(self, patern):
        self.pattern = patern
    